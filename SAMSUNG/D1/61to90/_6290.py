

# 초성 구간을 정의한 튜플 리스트
dicBase = (
    ('가', '깋'), ('나', '닣'), ('다', '딯'), ('라', '맇'), ('마', '밓'), ('바', '빟'), ('사', '싷'),
    ('아', '잏'), ('자', '짛'), ('차', '칳'), ('카', '킿'), ('타', '팋'), ('파', '핗'), ('하', '힣')
    )

# 분류할 단어 리스트
inputWord = [
    '막', '부모님', '비용', '비행기', '원래', '처리', '최초', '꼴', '좀', '들다', '싶다',
    '수출', '계시다', '다', '뒤', '듣다', '함께', '아이', '무척', '보이다', '가지다', '그',
    '자르다', '데리다', '마리', '개', '정도', '옳다', '놀이', '뜨겁다'
    ]

# 문제의 출력 예시처럼 '가'로 시작하는 단어들을 가나다순으로 정렬
# sorted_inputWord = sorted(inputWord)

# dicBase의 각 초성 구간을 순회하며 리스트 내포를 사용
result = [[word for word in inputWord if start <= word[0] <= end] for start, end in dicBase]

# '꼴', '뜨겁다'와 같이 dicBase에 없는 초성(ㄲ, ㄸ 등)은 빈 리스트로 처리됨

# 결과 출력
print(result)